'use strict';

angular.module('angularLocalStoragesApp', [
  'ngRoute',
  'angularLocalStorage',
])
  .config(function ($routeProvider) {
    $routeProvider
      .when('/', {
        templateUrl: 'views/main.html',
        controller: 'MainCtrl'
      })
      .otherwise({
        redirectTo: '/'
      });
  });
